# タプルの例
print((1, " あんぱん", "150円", "パン工場"))

# リストの例
print([1, " あんぱん", "150円", "パン工場"])

# タプルの例
menu = ("あんぱん", "食パン", "カレーパン", "メロンパン")
print(menu)

# タプルでインデックスを使用する例
countries = ("JP", "US", "CN", "GB")
print(countries[3])

# 辞書の例
countries = {"日本": "Japan", "アメリカ": "America", "カナダ": "Canada"}
print(countries["日本"])

# valueがリスト型の辞書
nameList = {"山田 太郎": ["男性", "168cm"], "佐藤 花子": ["女性", "159cm"]}
print(nameList["山田 太郎"])
