# 足し算の例
print(1 + 1)

# print関数を使用しない例
1 + 1

# 足し算ができない例
print("1 + 1")

# 文字列を連結する例
print("私は" + "20歳です")

# 文字列と数値の連結に失敗する例
print("私は" + 20 + "歳です")

# 数値と文字列の連結に失敗する例
print(20 + "18")

# 文字列を数値に変更する例
print(20 + int("18"))

# 引き算の例
print(3 - 1)

# かけ算の例
print(3 * 4)

# 割り算の例1
print(6 / 3)

# 割り算の例2
print(4 / 3)

# 割りきれない場合の例
print(16 // 3)
print(16 % 3)

# かけ算が優先される例
print(5 + 3 - 2 * 4)
